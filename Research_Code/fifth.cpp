long long int get_alpha(int r, int n = 1)
{
    long long int alpha;
    alpha = pow(2, r) - 2;
    return alpha;
}

void print_vector(Matrix input)
{
    for (const auto &row : input)
    {
        std::cout << "[";
        for (size_t i = 0; i < row.size(); ++i)
        {
            std::cout << row[i];
            if (i < row.size() - 1)
                std::cout << ",";
        }
        std::cout << "],";
    }
}

void printPascalPyramid(int rows)
{
    vector<vector<long long>> triangle(rows);

    for (int i = 0; i < rows; i++)
    {
        triangle[i].resize(i + 1);
        triangle[i][0] = triangle[i][i] = 1; // края равны 1

        for (int j = 1; j < i; j++)
        {
            triangle[i][j] = triangle[i - 1][j - 1] + triangle[i - 1][j];
        }
    }

    // Вывод в виде пирамиды
    int width = 4; // ширина для каждого числа
    for (int i = 0; i < rows; i++)
    {
        // Отступы слева для центрирования
        cout << string((rows - i - 1) * width / 2, ' ');

        for (int j = 0; j <= i; j++)
        {
            cout << setw(width) << triangle[i][j];
        }
        cout << endl;
    }
}

